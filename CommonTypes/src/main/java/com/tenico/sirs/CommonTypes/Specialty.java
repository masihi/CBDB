package com.tenico.sirs.CommonTypes;

public class Specialty {
	private String name;
	private SpecialityGroup group;
	
	public Specialty(String name, SpecialityGroup group) {
		
		this.group = group;
		this.group = group;
	}
	
	public String getName() {
		return name;
	}
	public SpecialityGroup getGroup() {
		return group;
	}

}
